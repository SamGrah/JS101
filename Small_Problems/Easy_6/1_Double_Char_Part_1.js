function repeater(str) {
  return str.split('').reduce((acc, val) => acc.concat(val, val), []).join('');
}

console.log(repeater('Hello'));        // "HHeelllloo"
console.log(repeater('Good job!'));    // "GGoooodd  jjoobb!!"
console.log(repeater(''));             // ""